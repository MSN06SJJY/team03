                <div class="toggle-box" id="sdgs3">
                    <li>3.良好健康與福祉<br><img src="sdgs3.jpg" height="107.2" width="160"/>「確保及促進各年齡層健康生活與福祉」</li>
                    <div class="content" id="content-sdgs3">
                        3.1　2030年前，全球孕產婦死亡率降低至每十萬活產中少於70例。<br>
                        3.2　2030年前，消除新生兒和五歲以下兒童的可預防死亡率，所有國家將新生兒死亡率降低至每千人死亡12人，五歲以下兒童死亡率降低至每千人死亡25人。<br>
                        3.3　2030年前，終結愛滋病、肺結核、瘧疾以及受忽視的熱帶性疾病，並對抗肝炎、水傳染性疾病以及其他傳染疾病。<br>
                        3.4　2030年前，透過預防、治療，以及促進心理健康與福祉，將非傳染性疾病導致的過早死亡率降低三分之一。<br>
                        3.5　強化物質濫用的預防與治療，包括麻醉藥品濫用以及酗酒。<br>
                        3.6　2020年前，全球道路交通事故造成的死傷人數減半。<br>
                        3.7　2030年前，確保性和生殖醫療保健服務的普遍性與可取得性，包括家庭計畫、資訊與教育，並將生育保健納入國家策略與計畫。<br>
                        3.8　實現全民醫療保險，包括財務風險保障，提供所有人高品質的基本保健服務，以及安全、有效、優質、可負擔的基本藥品和疫苗。<br>
                        3.9　2030年前，大幅減少危險化學物質、空氣污染、水污染、土壤污染以及其他污染造成的死亡及疾病人數。<br>
                        3.a　加強所有國家執行與落實「世界衛生組織菸草控制框架公約（World Health Organization Framework Convention on Tobacco Control）」。<br>
                        3.b　針對主要影響開發中國家的傳染病和非傳染性疾病，支援疫苗以及醫藥研發，並依據杜哈宣言提供負擔的起的基本藥物與疫苗；杜哈宣言中，發展中國家可充分利用國際專利規範——與貿易有關之智慧財產權協定（TRIPS），以保護民眾健康，尤其是必須提供醫藥管道給所有人。<br>
                        3.c　大幅增加開發中國家醫療保健的融資與借款，以及醫療保健從業人員的招募、培訓以及留任，尤其是最低度開發國家（LDCs）與小島嶼發展中國家（SIDS）。<br>
                        3.d　加強所有國家早期預警、減少風險，以及國家和全球健康風險的管理能力，特別是開發中國家。<br>
                    </div>
