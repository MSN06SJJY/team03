                <div class="toggle-box" id="sdgs4">
                    <li>4.優質教育<br><img src="sdgs4.jpg" height="107.2" width="160"/>「確保有教無類、公平以及高品質的教育，及提倡終身學習」</li>
                    <div class="content" id="content-sdgs4">
                        4.1　2030年前，確保所有的男孩和女孩都完成免費、公平和優質的中小學教育，取得相關且有效的學習成果。<br>
                        4.2　2030年前，確保所有的孩童都能獲得高品質的幼兒發展、照護及學前教育，為接受小學教育做好準備。<br>
                        4.3　2030年前，確保所有的男女都有公平、可負擔、高品質的技職、職業與高等教育機會，包括大學教育。<br>
                        4.4　2030年前，大幅增加掌握技術和職業技能的青年與成年人人數，以備就業、正式工作和創業所需。<br>
                        4.5　2030年前，消除教育上的性別差距，並確保弱勢族群可以平等地接受各層級教育與職業訓練，包括身心障礙者、原住民以及弱勢孩童。<br>
                        4.6　2030年前，確保所有的青年及大部分成年人，不論男性女性，都具備識字以及算術能力。<br>
                        4.7　2030年前，確保所有學子都能獲得永續發展所需的知識與技能，包括永續發展教育、永續生活模式、人權、性別平等、促進和平與非暴力文化、全球公民意識、尊重文化多樣性，以及文化對永續發展的貢獻。<br>
                        4.a　建立及提升適合孩童、身心障礙者以及性別敏感的教育設施，並為所有人提供安全、非暴力、有教無類、以及有效的學習環境。<br>
                        4.b　2020年前，大幅增加全球發展中國家的獎學金數目，尤其是最低度開發國家（以下簡稱LDCs）、小島嶼發展中國家（以下簡稱SIDS）與非洲國家，提升當地高等教育受教率，包括已開發國家與其他發展中國家的職業訓練、資訊與通信科技（ICT）研發、技術、工程和科學項目。<br>
                        4.c　2030年前，大幅增加合格師資人數，包括在發展中國家進行國際師資培訓合作，尤其是LDCs與SIDS。<br>
                    </div>
                </div>
                <div class="toggle-box" id="sdgs5">    
                    <li>5.性別平等<br><img src="sdgs5.jpg" height="107.2" width="160"/>「實現性別平等，並賦予婦女權力」</li>
                    <div class="content" id="content-sdgs5">
                        5.1　終結所有對婦女和女童的各種形式歧視。<br>
                        5.2　消除在公共和私人領域對女性的各種形式的暴力，包括人口走私、性剝削及其他形式的剝削。<br>
                        5.3　消除各種有害的習俗，例如童婚、未成年結婚、強迫結婚，以及女性割禮。<br>
                        5.4　透過提供公共服務、基礎建設與社會保護政策，認可並重視無償的照護和家務工作，並依各國國情，推動家人應共同分擔家事責任。<br>
                        5.5　確保婦女能充分、有效地參與政治、經濟、公共決策，並在各層級都享有參與決策領導的平等機會。<br>
                        5.6　根據國際人口與發展會議（ICPD）行動計畫、北京行動平台，及其檢討成果書，確保人民普遍享有性、生育健康與生育權利。<br>
                        5.a　根據國家法律進行改革，賦予婦女平等的經濟資源權利，以及獲得土地與其他形式的財產、金融服務、繼承與自然資源的所有權與掌控權。<br>
                        5.b　加強科技使用能力，特別是資訊與通訊技術（ICT），以提升婦女權力。<br>
                        5.c　採用及強化完善的政策與可執行的立法，以促進兩性平等，並提升各個階層女性的權力。<br>
                    </div>
                </div>
                <div class="toggle-box" id="sdgs6">
                    <li>6.清潔水與衛生<br><img src="sdgs6.jpg" height="107.2" width="160"/>「確保所有人都能享有水、衛生及其永續管理」</li>
                    <div class="content" id="content-sdgs6">
                        6.1　2030年前，實現所有人均能普遍和公平獲得安全且可負擔的飲用水。<br>
                        6.2　2030年前，讓每一個人都享有合適且平等的衛生設備，並杜絕露天大小便行為，特別注意婦女、女童及弱勢族群的需求。<br>
                        6.3　2030年前，透過減少污染、消除傾倒廢物、減少危險化學物質與材料釋放等方式改善水質，將未處理廢水的比例減半，並提高全球水資源回收率與安全再利用率。<br>
                        6.4　2030年前，大幅提升各個產業的用水效率，確保永續的淡水供應與回收，以解決水資源短缺，並大幅減少面臨缺水問題的人數。<br>
                        6.5　2030年前，全面實施一體化的水資源管理，包括適時地跨界合作。<br>
                        6.6　2030年前，保護及恢復與水有關的生態系統，包括山脈、森林、濕地、河流、含水層和湖泊。<br>
                        6.a　2030年前，擴大對開發中國家的國際合作與能力培養支援，協助其水資源、衛生相關的活動和計畫，包括雨水蓄集、海水淡化、提高用水效率、廢水處理、水資源回收再利用技術。<br>
                        6.b　支援及強化地方社區參與，以改善水與衛生的管理。<br>
                    </div>
                </div>
                <div class="toggle-box" id="sdgs7">
                    <li>7.可再生能源<br><img src="sdgs7.jpg" height="107.2" width="160"/>「確保所有的人都可取得負擔得起、可靠、永續及現代的能源」</li>
                    <div class="content" id="content-sdgs7">
                        7.1　2030年前，確保所有人都能取得可負擔、可靠、和現代化的能源服務。<br>
                        7.2　2030年前，大幅增加再生能源在全球能源結構中的比例。<br>
                        7.3　2030年前，使全球能源效率改善率成長一倍。<br>
                        7.a　2030年前，加強國際合作，以提升清潔能源研究與技術，包括可再生能源、能源效率、更先進與清潔的石化燃料科技，並促進對能源基礎建設與清潔能源技術的投資。<br>
                        7.b　2030年前，擴大基礎建設並升級技術，向開發中國家提供現代化及永續的能源服務，尤其是最低度開發國家（LDCs）、小島嶼發展中國家（SIDS）和內陸開發中國家（LLDCs）。<br>
                    </div>
                </div>
                <div class="toggle-box" id="sdgs8">
                    <li>8.體面工作與經濟增長<br><img src="sdgs8.jpg" height="107.2" width="160"/>「促進包容且永續的經濟成長，讓每個人都有一份好工作」</li>
                    <div class="content" id="content-sdgs8">
                        8.1　依據國情維持人均經濟成長，尤其最低度開發國家（以下簡稱LDCs）的國內生產毛額（GDP）成長率，每年至少需達7%。<br>
                        8.2　透過多元化、技術升級與創新，實現更高水平的經濟生產力，包括將焦點集中在高附加價值與勞動密集產業。<br>
                        8.3　推動開發導向的政策，促進支持生產活動、創造合宜就業機會、創業精神、創意與創新，並透過提供財務服務等方式，鼓勵微型與中小企業實現正規化與成長。<br>
                        8.4　2030年前，逐步改善全球的資源使用與生產效率，依據「永續消費和生產的十年計畫（10-year framework of programmes）」，努力使經濟增長與環境退化脫鉤，且已開發國家應成為表率。<br>
                        8.5　2030年前，實現全面生產性就業，且每個成年人都能獲得合宜工作，包括青年與身心障礙者，並具同工同酬的待遇。<br>
                        8.6　2020年前，大幅降低失業、未受教育或培訓的青年比例。<br>
                        8.7　立即採取有效措施來消除強迫勞動、結束現代奴隸制度、人口販賣，確保禁止與消除最惡劣形式的童工，包括童兵的招募使用，並在2025年終結一切形式的童工。<br>
