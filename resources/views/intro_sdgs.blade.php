                <div class="toggle-box" id="sdgs2">
                    <li>2.零飢餓<br><img src="sdgs2.jpg" height="107.2" width="160"/>「確保糧食安全，消除飢餓，促進永續農業」</li>
                    <div class="content" id="content-sdgs2">
                        2.1　2030年前，消除飢餓，確保所有的人全年都有安全、營養且足夠的糧食，特別是窮人和弱勢族群（包括嬰兒）。<br>
                        2.2　2030年前，消除所有形式的營養不良。包括：2025年之前達成為五歲以下兒童發育遲緩、消瘦訂定的國際目標，並解決青少女、孕婦、哺乳婦女以及老年人的營養需求。<br>
                        2.3　2030年前，使農村的生產力與小規模糧食生產者的收入皆翻倍成長，尤其是婦女、原住民、家庭農民、牧民與漁民，作法包括讓他們安全及公平地獲取土地、生產資源、知識、金融服務、非農業就業市場及增值機會。<br>
                        2.4　2030年前，確保建立可永續發展的糧食生產系統，實施可增強生產力、具彈性的農作方式，協助維護生態系統，強化適應氣候變遷、極端氣候、乾旱、洪水與其他災害的能力，並逐步改善土地和土壤品質。<br>
                        2.5　2020年前，維持種子、栽種植物、家畜以及與他們有關的野生品種之基因多樣性。包括透過國家、區域和國際層級的政策，妥善管理多樣化的種子和植物銀行，並促進基因資源與相關傳統知識利用，產生的好處依照國際協議公平地分享。<br>
                        2.a　透過加強國際合作，提高在鄉村基礎建設、農業研究、推廣服務、科技發展、動植物基因銀行上的投資，以改善發展中國家的農業產能，尤其是最低度開發國家。<br>
                        2.b　根據杜哈回合貿易談判（Doha Development Round）的共識，糾正並防止全球農業市場的貿易限制和扭曲，包括消除各種形式的農業出口補貼和具同等效力的出口措施。<br>
                        2.c　採取措施，確保糧食商品市場及其衍生品正常運作，並促進及時獲得市場資訊（包括糧食儲備量），來抑制極端的糧食價格波動。<br>
                    </div>
