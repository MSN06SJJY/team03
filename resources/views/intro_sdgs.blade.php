                        17.1　強化國內資源動員，包括透過向開發中國家提供國際支援，改善其國內稅收、和其他收益取得能力。<br>
                        17.2　已開發國家充分履行其官方發展援助承諾，包括向開發中國家提供國民所得毛額（GNI）中的0.7％作為政府開發援助（ODA），以及向最低度開發國家（以下簡稱LDCs）提供國民所得毛額中的0.15至0.2％為開發援助。鼓勵開發援助提供方設定目標，提供佔國民所得毛額至少0.2%的開發援助給LDCs。<br>
                        17.3　為開發中國家多方籌集額外的財務資源。<br>
                        17.4　透過政策協調，酌情推動債務融資、債務減免和債務重整，協助開發中國家實現長期債務永續性，並處理高負債貧窮國家的外部負債，以減輕其負債壓力。<br>
                        17.5　為LDCs採用及實施投資促進方案。<br>
                        17.6　加強在科學、技術和創新領域的南－北、南－南、三角形區域與國際合作，以及強化依照相互議定條件提高知識交流，作法包括改善現有機制之間的協調（特別在聯合國層級），以及透過全球技術促進機制加強協調。<br>
                        17.7　按照共同議定原則，使用有利的條款和條件，包括特許權和優惠條款，針對開發中國家促進環保科技的發展、轉移、流通及擴散。<br>
                        17.8　2017年前，全面落實開發LDCs的技術庫，建立科學、科技與創新能力培養機制，並擴大其科技使用，尤其是資訊傳播科技（ICT）。<br>
                        17.9　強化國際支持，為開發中國家實施有效且鎖定目標的能力培養，以支持各國落實各項永續發展目標的國家計畫，包括南－北、南－南和三方合作。<br>
                        17.10　在世界貿易組織（WTO）下，建立一個全球性、遵循規則、開放、無歧視以及公平地多邊貿易系統，包括透過杜哈發展議程完成協商。<br>
                        17.11　大幅增加開發中國家的出口量，尤其是在2020年前，讓LDCs的全球出口占比增加一倍。<br>
                        17.12　按照世界貿易組織之決策，如期對所有LDCs實施持續性免關稅、免配額的市場進入管道，包括適用於LDCs進口的原產地優惠規則，必須簡單且透明，有助其進入市場。<br>
                        17.13　透過政策協調與一致性來加強全球宏觀經濟穩定性。<br>
                        17.14　加強永續發展政策的一致性。<br>
                        17.15　尊重每個國家的政策空間和領導權，以建立及執行消除貧窮與永續發展的政策。<br>
                        17.16　透過多邊合作加強促進永續發展的全球夥伴關係，動員和分享知識、專業、科技與財務資源，支持所有國家、尤其是開發中國家實現永續發展目標。<br>
                        17.17　以夥伴關係的經驗和籌資策略為基礎，鼓勵和促進有效的公共、公私營和民間社會夥伴關係。<br>
                        17.18　2020年前，加強協助開發中國家、包括LDCs與小島嶼發展中國家（SIDS）建立能力，大幅增加其獲取高品質、即時且可靠的數據，包括按收入、性別、年齡、種族、族裔、移民、身心障礙、地理位置以及各國其他人口分類的各項數據取得。<br>
                        17.19　2030年前，依據現有措施，制定衡量永續發展進程的方式，使國內生產總值（GDP）計算更為完善，並協助開發中國家培養統計能力。<br>
