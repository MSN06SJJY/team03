                    <!-- 下拉的進階內容 -->
                    <div class="content" id="content-sdgs1">
                        1.1　2030年前，消除世界各地所有人的極端貧窮（目前定義為每人每日生活費不足1.25美元）。<br>
                        1.2　2030年前，依據各國界定標準，各年齡層的男人、婦女和兒童貧窮人數至少減半。<br>
                        1.3　依各國國情實施合適的全民社會保障制度和措施，包括社會底層人民，到西元2030年能涵蓋大範圍的貧窮、弱勢族群。<br>
                        1.4　到西元2030年，確保所有人，尤其是貧窮與弱勢族群，能享有平等獲得經濟資源的權利，涵蓋層面包含經濟資源、基本服務、土地和其他形式財產的所有權、控制權、繼承權、自然資源、新科技和金融服務（包括微型貸款）。<br>
                        1.5　到西元2030年，提升貧窮與弱勢族群的韌性和災後復原能力，減少他們遭受極端氣候、經濟、社會和環境衝擊與災害。<br>
                        1.a　確保各個地方的資源能夠大幅動員，包括加強發展合作，為發展中國家，尤其是最低度開發國家（LDCs），提供妥善且可預測的方法，以實施計畫與政策全面消除國內貧窮。<br>
                        1.b　依據考量到貧窮與性別的發展策略，建立國家、區域、國際層級的健全政策框架，加速消除貧窮行動的投資。<br>
                    </div>
